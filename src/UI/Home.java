    void setMenu() {

        lblHinh.setBounds(0, 0, 824, 762);
        SetImageSize(2);
        tm = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                SetImageSize(x);
                x += 1;
                if (x >= list.length) {
                    x = 0;
                }
            }

        });

        tm.start();

    }

